/**
 * MCP Adapter Service
 * Bridges the tool provider registry with actual MCP implementations
 * Handles tool invocation for nutrition and fitness MCP services
 */

/**
 * Invoke a nutrition MCP tool
 * Maps tool names to actual MCP nutrition controller methods
 */
async function invokeMcpNutritionTool(toolName, input) {
  try {
    if (!input || !input.trim()) {
      throw new Error('Nutrition input is required');
    }

    // Route to appropriate nutrition handler
    switch (toolName) {
      case 'meal_analysis':
        return await analyzeMeal(input);
      case 'nutrition_plan':
        return await generateNutritionPlan(input);
      case 'ingredient_search':
        return await searchIngredients(input);
      case 'dietary_insights':
        return await getDietaryInsights(input);
      default:
        throw new Error(`Unknown nutrition tool: ${toolName}`);
    }
  } catch (error) {
    console.error('[MCP Adapter] Nutrition tool error:', { toolName, error: error.message });
    throw error;
  }
}

